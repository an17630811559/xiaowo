package com.awb.base;

public enum ApiStyleEnum {
    NONE("none",0);

    private String name;
    private int value;

    ApiStyleEnum(String name, int value) {
        this.name = name;
        this.value = value;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public int getValue() {
        return value;
    }

    public void setValue(int value) {
        this.value = value;
    }
}
